                	<h2>Introduction to Intellectual Property for Start Ups</h2>
                    <p>By <a href="blog-inner.php">AbuBakar Butt</a> Category <a href="blog-inner.php">News Posted</a> Date <em class="date">February 29, 2016</em></p>
                    <p>Building a startup is no minor feat and the owner has their hands in so many different places at the same time that it seems near impossible to turn their dream into a reality. There are several legal issues to address one of which is integral to the success of your startup: Intellectual Property protection.</p>
                    <h3>What is Intellectual Property Protection?</h3>
                    <p>The basic concept is that you do not wish your original business ideas to be stolen by competitors or used by the general public in any way. That idea, although intangible, has commercial value and is your sole property unless you decide to sell it.</p>
                    <p>This is applicable to all kinds of brands and business; however, there are some considerations you must check before deciding that it’s worth protecting.</p>
                    <h3>Why does your startup need an IP strategy?</h3>
                    <p>Since a startup owner has their hands full, they may think that an IP strategy is either not worth the time and investment or that it’s just not that important. The truth could not be far from this.</p>
                    <p>Everything you build could come crashing down once the core idea of your business is copied by a competitor and they gain rights to it. Once some other company gains rights to your original idea and presents it as their own, you lose the chance to declare that it was actually your property. This may seem unfair to you but if someone coincidentally came up with the same idea as you or stole your idea, it became theirs because they obtained legal rights to it. The origin of the idea is irrelevant so now you are left empty handed because you ignored the need for an IP strategy.</p>
                    <p>Once your idea becomes public domain, you lose all claims to it.</p>
                    <p>Moral: Establish an IP strategy pronto!</p>
                    <h3>Types of IP protection</h3>
                    <h4>Patents</h4>
                    <p>Get in touch your IP body and gain their expertise on a suitable patent search if it is relevant to your company. Narrow down your search to ensure you are not infringing on anyone’s rights and see what’s out there from your competitors in the market. This will give you a pretty good picture of what you are up against and inspire you to select ideas of your own for patenting.</p>
                    <p>Examine your assets keenly and ask yourself if any of your business ideas worth being patented. Obviously, you cannot patent every single thing so pick out the one which is most unique to your brand and maybe defines you as a business. Choose something that sets you apart from your competitors and helps you establish yourself in the market.</p>
                    <h4>How to tell if your idea is eligible for a patent?</h4>
                    <p>Before you jump to get a patent, your idea must meet the following requirements:</p>
                    <ol>
                        <li>It must be functional and already executed </li>
                        <li>It must never have been patented</li>
                        <li>It cannot have been described in published print before</li>
                        <li>It must prove its usefulness</li>
                    </ol>
                    <h4>Types of Patents</h4>
                    <p>There are two main categories of patents available depending on the nature of your idea and its significance in the business sphere. They may cost you tens of thousands of dollars on a national level so make sure you do it right.</p>
                    <h4>1. Utility patents:</h4>
                    <p>You may be granted a utility patent for around twenty years before it expires. These are appropriate for good inventions in machinery, manufactures, development procedures and chemical substances.</p>
                    <h4>2. Design patent</h4>
                    <p>This lasts for around fifteen years from when you were first granted it. As is obvious from its name, a design patent protects your rights to any design you have created for a product your company manufactures. These are relatively inexpensive and fast.</p>
                    <h4>Trademark</h4>
                    <p>A company trademark is a huge investment so it is not to be taken lightly especially by a startup operating with limited funds. Before you proceed, ascertain that it is indeed available for trademark and not owned by any other brand. Be perfectly certain that you are doing this for the long run and won’t be giving up on this trademark in the near future. You need to understand that your particular trademark has to stand out from the rest of your competitors in the market. This is an excellent way to literally “make your mark in the world”.</p>
                    <p>Trademark words, names, logos or designs are widely known. Consumers and brands both are familiar with some famous trademarks such as logos of companies like Coca-Cola, Shell, BMW, Ferrari, Audi, Apple, Bayer, SONY, Toblerone, Microsoft, Lacoste and many more. Slogans such as “I’m lovin’ it” (Mcdonalds), “Just do it” (Nike) and “Et fresh” (Subway) are also very popular.</p>
                    <h4>Copyright </h4>
                    <p>This is for tangible property especially the written word and images. Anything that is available in print or electronic form such as website design, source code and web content. This is well known in literary circles and the media.</p>
                    <p>It is quite affordable as compared to other means of protecting intellectual property. Anyone infringing on your copyrights is liable to pay hundreds of thousands of dollars worth as compensation.</p>
                    <h4>Trade Secrets</h4>
                    <p>These are often jealously guarded and the envy of competitors everywhere for good reason. They make you the exclusive provider of that particular product or service. It piques the curiosity of consumers and competitors both boosting your market value and enhancing your brand identity. </p>
                    <p>These may include but are not limited to manufacturing details, formulas, client particulars, development procedures, marketing strategies, etc.</p>
                    <p>Trade secret protection entails some work for the owner because you must take strong measures to ensure your secret does not leak out. This often involves employees signing nondisclosure documents, limiting access to that trade secret within your company and setting up software security programs.</p>
                    <p>There are several famous trade secrets with much speculation surrounding them such as Google’s search algorithm, KFC’s Secret Recipe of 11 Herbs and Spices, Coca-Cola’s formula, Mrs. Fields chocolate chip cookie recipe, Thomas’ English Muffins recipe, etc.</p>
                    <h3>How to build an IP strategy?</h3>
                    <p>If this is your first time managing a startup, you may not be well versed in business legalities. That’s okay as long as you have a good consultant who is both experienced and referred to you by reliable sources. </p>
                    <ul>
                        <li>You must identify what needs to be protected</li>
                        <li>Figure out the best way to protect your valuable assets</li>
                        <li>Get an early start on the IP protection process</li>
                        <li>Remain aware of IP strategies of peers and competitors</li>
                        <li>Learn from the best in the business</li>
                        <li>Make sure your confidentiality agreements are thorough</li>
                        <li>Understand the risks of using open source software</li>
                        <li>Contact an IP consultant as soon as possible</li>
                        <li>Do not commercialize your IP without protecting it fully </li>
                        <li>Plan for the future not just the present</li>
                    </ul>
                    <p>Staying on top of your IP protection strategy will keep you well informed and open to improvisations or compromises when unexpected situations arise. This is one step of building a startup you cannot ignore for that could lead to dire consequences.</p>
