                	<h2>How Uber/Airbnb Got its First 100,000 Users</h2>
                    <p>By <a href="blog-inner.php">AbuBakar Butt</a> Category <a href="blog-inner.php">News Posted</a> Date <em class="date">February 29, 2016</em></p>
                    <p>Every startup dreams of making it big in their industry. Airbnb and Uber are two startups that have changed the face of their respective industries and accomplished in a few months what takes established companies years to achieve.</p>
                    <p>Want to know who they did it? Read on to find out.</p>
                    <h3>Airbnb</h3>
                    <p>The humble beginnings of Airbnb were born out of personal needs. Like they say, “Necessity is the mother of invention”. Brian Chesky and Joe Gebbia could never have imagined that a last resort measure taken at a desperate time would lead them to own a billion dollar startup.</p>
                    <p>In 2007, they were no longer able to afford their San Francisco apartment’s rent so they decided to put it up for those seeking lodging. They were not that fond of Craiglist so they set up their own website for this purpose. After a successful weekend with three renters, they began receiving emails asking if their services were available in other parts of the world. The main idea took root and there was no stopping them after that.</p>
                    <p>Later that year they planned to officially launch their new venture, Airbed & Breakfast, with help of their engineer-old roommate, Nathan Blecharczyk, targeting the users who would need their services owing to the lack of space at the time of the Democratic National Convention. Even with a good start they faced bad times. To raise $30K for their startup, they devised a clever marketing stunt around election time selling cereal boxes repackaged as Obama O’s and Cap’n McCain’s at convention parties.</p>
                    <p>Collaboration with Y Combinator in 2009 brought them $20K and a new name: Airbnb. Soon after that they got $600K from Sequoia Capital and Y Ventures.</p>
                    <h3>Airbnb/Craigslist integration</h3>
                    <p>The turning point in Airbnb’s success came when they concocted a brilliant but difficult plan to integrate with Craiglist, as it was their biggest competitor and they badly needed its massive user base. They believed they could offer much better services than Craiglist and people would be willing to convert to Airbnb if only they could reach their target audience. Not only where their services more attractive but users made more money renting out properties on Airbnb than Craiglist. Needless to say, the plan was a resounding success.</p>
                    <h3>Spamming Craiglist </h3>
                    <p>It was not good enough to just integrate with Craiglist, they wanted more so Airbnb developed a sneaking spamming strategy. Airbnb was able to reach Craiglist users and offer them a much better alternative as they thought. The comparison was obvious and more and more users found themselves drifting towards Airbnb. It was practicality effortless to make profit without spending a single cent. This made a huge difference in expanding Airbnb’s user base.</p>
                    <h3>Mobile circle</h3>
                    <p>It would have been foolish to ignore how quickly the number of mobile users of Airbnb was increasing so they launched their own mobile app in 2013. It has been updated and filed by mobile app developers with useful in-demand features to provide the best possible user experience to Airbnb’s consumers. There are multiple options for creating a listing, uploading property photos, filtering searches, etc. Mobile users are a huge part of the user base of Airbnb so this mobile venture has brought them a lot of profit.</p>
                    <h3>Optimized website content</h3>
                    <p>Airbnb has set up a system to let the best listings be visible on top and the more shady listings to be hard to find. Not only is that but community tools available for users so that they may also be able to filter out the good listings from the not-so-good ones.</p>
                    <h3>Clear guidelines</h3>
                    <p>An excellent strategy to increase reliability and make their website appear more attractive was developed by Airbnb. They put up guidelines for users listing their properties so that they would know exactly how to do it and make it look good. They also replaced unattractive property photos with better ones so that users took the cue from them and improved the appearance of their listings increasing their chances of a positive response. </p>
                    <h3>Community connections</h3>
                    <p>Airbnb realizes that users drive business profits so they made their website more productive, personal and welcoming by establishing user communities. Here users could freely interact with each other, share opinions and advice. People were willing to reach out to others for mutual benefit and learning new things.</p>
                    <h3>Social interaction data</h3>
                    <p>Airbnb exploited the fact that we live in the age of social media by gathering useful data from popular social media sites like Facebook. It is crucial to show other people just how wide your user base is and how many people like and recommend your services. Social media is an excellent method to do just that. Airbnb checked for mutual connections between users both renters and those seeking properties for rent. They made these people as if they knew each other well increasing their sense of reliability and community. This leads to better business for Airbnb.</p>
                    <h3>Customer referral programs</h3>
                    <p>This is a wonderful way to enhance your marketing strategy and Airbnb has done it extremely well. If you like a service, you would probably recommend it to a contact that is looking for a similar service. However, what happens when you have an incentive to refer that service you are happy with to a friend, family member, colleague, neighbor, classmate or anyone else you know? Let’s take this a notch up and say that the person you refer the service to also gets an incentive for accepting your referral. All three parties involved would benefit from this. Aairbnb offers $25 credit to the referee and the same amount to the person who accepts that referral.</p>
                    <h3>Uber</h3>
                    <p>An on-demand taxi service brings a lot of convenience to people and it obviously generates a lot of profit if it does its job well. Uber is one startup that quickly became a household name because it offered reliable services and its marketing campaigns packed a serious punch.</p>
                    <h3>Wiping out the competition</h3>
                    <p>Uber is notorious for its aggressive marketing tactics involving the annihilation of its major competitor, Lyft, by snatching its target audience and making it its own. There have been years of bad blood between these two ruthless competitors often resorting to questionable tactics to ensure their own hold over the market. In 2014, there were CNN reports of Uber employees deliberately booking thousands of rides and cancelling them.  Even though Uber denied its attempt to sabotage the competition, Lyft insisted that it had proof through cross referencing phone numbers.</p>
                    <p>This wasn’t the first or the first or the last time such a story emerged. According to The Verge, operation SLOG was launched by Uber. Volunteer contractors called brand ambassadors were hired and promised great incentives for taking part in this new program. These brand ambassadors secretly took rides with Lyft drivers and those of other competitors while trying to recruit them for Uber.</p>
                    <h3>Smooth transactions</h3>
                    <p>Even with the risk of monetary theft and hacking into private user accounts, Uber has managed to retain the trust of its users by introducing a good payment process which is both secure and convenient for users.</p>
                    <h3>Referral programs</h3>
                    <p>In order to increase the number of referrals and thus profit, Uber offers an incentive of $30 to the referee as well as the person who accepts their referral. This means that there is no ill will between the two parties and no one feels used. In fact, Uber, the referee and the one who accepts it all win in this situation.</p>
                    <h3>Mobile strategy</h3>
                    <p>Uber has done a great job of capturing the population of mobile users interested in their services. Their mobile app is constantly updated and offers a number of useful features to book, confirm and cancel rides as well as other relevant options.</p>
                    <p>Although from different industries, Uber and Airbnb have proven that it is not impossible for any new startup to gain a strong foothold in the market and become successful in a short time.</p>
